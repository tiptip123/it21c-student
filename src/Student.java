
public class Student {

    String name, section, motto;

    String name() {

        return name;

    }

    String getSection() {

        return section;

    }

    String getMotto() {

        return motto;

    }

    void setName(String newName) {

        name = newName;

    }

    void setSection(String newSection) {

        section = newSection;

    }

    void setMotto(String newMotto) {

        motto = newMotto;

    }

}
